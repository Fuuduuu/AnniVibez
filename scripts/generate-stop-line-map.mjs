import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, '..');

const sourcePath = path.join(repoRoot, 'docs', 'audit', 'route-pattern-stopid-map.json');
const outPath = path.join(repoRoot, 'src', 'data', 'stopLineMap.js');

const LINE_COLORS = {
  '1': 'blue',
  '2': 'green',
  '3': 'yellow',
  '5': 'orange',
};

const STOP_ID_RE = /^[0-9]+-[0-9]+$/;

function sortLineNumbers(values) {
  return [...values].sort((a, b) => Number(a) - Number(b));
}

function sortStopIds(values) {
  return [...values].sort((a, b) => a.localeCompare(b, 'en'));
}

function uniqueStopIdsInOrder(stops) {
  const out = [];
  for (const stop of stops || []) {
    const stopId = String(stop?.stopId || '').trim();
    if (!STOP_ID_RE.test(stopId)) continue;
    out.push(stopId);
  }
  return out;
}

function toModuleSource({ lineColors, stopToLines, linePatterns, sourceRel }) {
  const header = [
    '// Auto-generated by scripts/generate-stop-line-map.mjs',
    `// Source: ${sourceRel}`,
    '',
  ].join('\n');

  return `${header}export const LINE_COLORS = ${JSON.stringify(lineColors, null, 2)};\n\nexport const STOP_TO_LINES = ${JSON.stringify(stopToLines, null, 2)};\n\nexport const LINE_PATTERNS = ${JSON.stringify(linePatterns, null, 2)};\n`;
}

async function main() {
  const sourceRaw = await fs.readFile(sourcePath, 'utf8');
  const source = JSON.parse(sourceRaw);
  const patterns = Array.isArray(source?.patterns) ? source.patterns : [];

  const stopToLinesMap = new Map();
  const linePatternsMap = new Map();

  for (const pattern of patterns) {
    const lineNumber = String(pattern?.lineNumber || '').trim();
    if (!lineNumber) continue;

    const stopIds = uniqueStopIdsInOrder(pattern?.stops);
    if (!linePatternsMap.has(lineNumber)) linePatternsMap.set(lineNumber, []);
    linePatternsMap.get(lineNumber).push({
      patternName: String(pattern?.patternName || '').trim(),
      sourceFile: String(pattern?.sourceFile || '').trim(),
      stopIds,
    });

    for (const stopId of stopIds) {
      if (!stopToLinesMap.has(stopId)) stopToLinesMap.set(stopId, new Set());
      stopToLinesMap.get(stopId).add(lineNumber);
    }
  }

  const stopToLines = {};
  for (const stopId of sortStopIds(stopToLinesMap.keys())) {
    stopToLines[stopId] = sortLineNumbers(stopToLinesMap.get(stopId));
  }

  const linePatterns = {};
  for (const lineNumber of sortLineNumbers(linePatternsMap.keys())) {
    linePatterns[lineNumber] = linePatternsMap.get(lineNumber);
  }

  const sourceRel = path.relative(repoRoot, sourcePath).replaceAll('\\', '/');
  const outSource = toModuleSource({
    lineColors: LINE_COLORS,
    stopToLines,
    linePatterns,
    sourceRel,
  });
  await fs.writeFile(outPath, outSource, 'utf8');

  const summary = {
    source: sourceRel,
    output: path.relative(repoRoot, outPath).replaceAll('\\', '/'),
    patternCount: patterns.length,
    stopToLinesCount: Object.keys(stopToLines).length,
    lineNumbers: Object.keys(linePatterns),
  };

  process.stdout.write(`${JSON.stringify(summary, null, 2)}\n`);
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
